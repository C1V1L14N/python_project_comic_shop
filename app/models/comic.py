class Comic:
    def __init__(self, name, author, genre, wholesale_price, markup, stock_count, min_count, out_of_stock, publisher, id = None):
        self.name = name
        self.author = author
        self.genre = genre
        self.wholesale_price = wholesale_price
        self.markup = markup
        self.stock_count = stock_count
        self.min_count = min_count
        self.out_of_stock = out_of_stock
        self.publisher = publisher
        self.id = id






